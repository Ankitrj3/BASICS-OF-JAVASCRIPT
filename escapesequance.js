
/*****
 \' single quote
 \" double quote
 \\ backslash
 \n new line
 \r carriage return
 \t tab
 \b backspace
 \f form feed
 */


 let a ="ank\'it";
 console.log(a);

 let b ="ank\"it";
 console.log(b);

 let c ="ank\\it";
 console.log(c);

 let d ="ank\nit";
 console.log(d);

 let e ="ank\bit";
 console.log(e);

 let f ="ank\tit";
 console.log(f);

