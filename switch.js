let a = prompt ("enter the days in the form of integers\n");
let daynumber=parseInt(a)
switch(daynumber){
    case 1:
        alert('monday');
        break;
    case 2:
        alert('tuesday');
        break;
    case 3:
        alert('wednesday');
        break;
    case 4:
        alert('thrusday');
        break;
    case 5:
        alert('friday');
        break;
    case 6:
        alert('saturday');
        break;   
    case 7:
        alert('sunday');
        break;  
}